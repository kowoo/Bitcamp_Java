package generic2;

public class Store2<A, B> {
	//두가지 타입도 가능하다.
	private A data1;
	private B data2;
	
	public A getData1() {
		return data1;
	}
	public void setData1(A data1) {
		this.data1 = data1;
	}
	public B getData2() {
		return data2;
	}
	public void setData2(B data2) {
		this.data2 = data2;
	}
	
	
	
	
}
